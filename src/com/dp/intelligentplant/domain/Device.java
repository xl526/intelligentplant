package com.dp.intelligentplant.domain;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

@Entity
@Table(name="T_DEVICE")
public class Device extends BaseDomain {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 5611937239558396831L;
	
	@Column(name="TAG")
	private String tag;

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}
	
}
